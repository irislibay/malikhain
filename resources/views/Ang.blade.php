                                <p class="text-justify">
                                    He was an only son with five sisters, born to Chinese immigrants from Fukien; Vicente Ang and Chin Lim. Worried about China’s fate against the invading Manchurians at that time, his father named him Kiukok, which means “save the country.”
                                </p>
                                <p class="text-justify">
                                    As a young man, Kiukok did comical sketches and also studied Chinese ink and brush painting methods. Kiukok also copied movie ads from newspapers while tending his father’s sari-sari store, a business that he was expected to someday run. Against his father’s wishes, Kiukok began his formal art studies at Manila’s University of Santo Tomas where Vicente Manansala was his primary mentor, and also became a lifelong friend. At the conclusion of his studies, Kiukok taught art in a school of overseas Chinese for five years.
                                </p>
                                <p class="text-justify">
                                    Kiukok’s first formal recognition came in the form of a third prize award in the Shell National Students Art Competition for Calesa in 1953. Then, at the urging of Vicente Manansala, Kiukok launched his first one-man show at the Contemporary Arts Gallery in 1954.
                                </p>
                                <p class="text-justify">
                                    In 1965 he and Vicente Manansala traveled to New York. Upon his return to the Philippines, his style took a turn towards a new vivid Expressionist that including themes of agony, sorrow and madness. The difficulty of these themes caused a temporary loss of interest on the part of art dealers and collectors.
                                </p>
