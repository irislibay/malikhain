                                    Ang Kiukok, an ethnic Chinese painter who studied under Vincente Manansala, attained prominence for an Expressionist style that fused Cubist, Surrealist and Expressionist aspects into a unique personal aesthetic. It has been said that he “opened eyes with his violent Cubist paintings of fighting cocks, stray dogs and tormented lovers,” and that his Expresionist works “sustained a hard-edged evocation of fortitude and indignation in a hostile setting of screaming men, crucifixions and junk.”
